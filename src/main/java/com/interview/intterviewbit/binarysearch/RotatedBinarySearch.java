	public int search(final List<Integer> a, int b) {
	    int beg = 0, end = a.size()-1;
	    
	    while(beg < end){
	        int mid = beg + end >> 1;
	        
	    	int midVal = a.get(mid);
	    	int begVal = a.get(beg);
	    	int endVal = a.get(end);
	    
	    	if(a.get(mid) == b){
	    		return mid;
	    	}
	    	
	        //second part is sorted
	        if(a.get(mid) <= a.get(end)){
	            if(a.get(mid) <= b && b <= a.get(end)){
	                beg = mid + 1;
	            }
	            else {
	                end = mid - 1;
	            }
	        }
	        //first part is sorted
	        else{
	            if(a.get(mid) >= b && b >= a.get(beg)){
	                end = mid - 1;
	            }
	            else{
	                beg = mid + 1;
	            }
	        }
	    }
	    
	    if(a.get(beg) != b){
	        return -1;
	    }
	    
	    return beg;
	}
	
